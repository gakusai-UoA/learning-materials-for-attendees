// Part 5 - ミドルウェアの最もシンプルな形（合言葉の関所）
app.get(
  "/secret", // ←パス
  async (c, next) => {
    // ←ミドルウェアの開始
    // [1] 前処理：合言葉をチェックする
    if (c.req.query("pass") !== "shakemake") {
      return c.text("合言葉が違います！", 401); // 追い返す
    }
    // [2] 合格！次の人（ハンドラ）へバトンタッチ
    await next();
  }, // ←ミドルウェアの終了
  (c) => {
    // [3] メインの処理：ここには「合格した人」しか来ない
    return c.text("秘密のメッセージ：IT部へようこそ！");
  },
);
